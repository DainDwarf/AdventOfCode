def create_tree(inp):
    tree = DiGraph()
    for line in inp.strip().split('\n'):
        lhs, rhs = line.strip().split(' => ')
        rhs_qty, rhs_mol = rhs.strip().split()
        for op in lhs.strip().split(', '):
            qty, mol = op.strip().split()
            tree.add_edge(rhs_mol, mol, output_qty=int(rhs_qty), input_qty=int(qty))
    assert is_directed_acyclic_graph(tree)
    return tree


def ore_for_fuel(tree, need=1):
    """Computes how much ore is needed for ``need`` fuel."""
    for node in tree.nodes:
        tree.nodes[node]['need'] = 0
    tree.nodes['FUEL']['need'] = need
    for node in topological_sort(tree):
        for output_node, input_node, stoichio in tree.out_edges(node, data=True):
            times = int(math.ceil(tree.nodes[output_node]['need']/stoichio['output_qty']))
            tree.nodes[input_node]['need'] += times*stoichio['input_qty']
    return tree.nodes['ORE']['need']


def partOne(inp):
    tree = create_tree(inp)
    return ore_for_fuel(tree)


def partTwo(inp):
    """Solve by dichotomy."""
    ore_limit = 1000000000000
    tree = create_tree(inp)
    for too_high in count():
        if ore_for_fuel(tree, 2**too_high) > ore_limit:
            break
    low = 2**(too_high-1)
    high = 2**too_high
    while high-low > 1:
        mid = (high+low)//2
        if ore_for_fuel(tree, mid) > ore_limit:
            high = mid
        else:
            low = mid
    return low


